with tab3:
    st.subheader("🔧 如何运行 Streamlit 服务")

    st.markdown("""
    ### 方式一：命令行启动（推荐）

    **Windows 用户：**

    1. 按 `Win + R`，输入 `cmd`，回车打开命令提示符

    2. 进入项目目录：
    ```
    cd C:\\ProgramData\\WorkBuddy\\chromium-env\\6od91a\\WorkBuddy\\2026-06-08-10-32-07\\job-hunter
    ```

    3. 激活 Python 虚拟环境（如果已创建）：
    ```
    C:\\ProgramData\\WorkBuddy\\chromium-env\\6od91a\\.workbuddy\\binaries\\python\\envs\\default\\Scripts\\activate
    ```

    4. 启动 Streamlit：
    ```
    streamlit run app.py
    ```

    5. 看到以下提示表示成功：
    ```
      You can now view your Streamlit app in your browser.
      Local URL: http://localhost:8501
    ```

    6. 打开浏览器访问 **http://localhost:8501**

    **Mac / Linux 用户：**

    ```bash
    cd job-hunter
    streamlit run app.py
    ```

    然后访问 http://localhost:8501

    ---

    ### 方式二：使用虚拟环境中的 Streamlit

    如果全局没有安装 Streamlit，使用项目自带的虚拟环境：

    **Windows：**
    ```
    C:\\ProgramData\\WorkBuddy\\chromium-env\\6od91a\\.workbuddy\\binaries\\python\\envs\\default\\Scripts\\streamlit.exe run app.py
    ```

    ---

    ### ⚠️ 常见运行问题

    **问题 1：提示 "streamlit 不是内部或外部命令"**
    - 解决方法：使用方式二的完整路径，或者先安装 `pip install streamlit`

    **问题 2：端口 8501 被占用**
    - 解决方法：指定其他端口
    ```
    streamlit run app.py --server.port 8502
    ```

    **问题 3：连接不稳定 / 上传简历时断开**
    - 项目已配置 `.streamlit/config.toml` 增加了超时时间
    - 如果仍然不稳定，尝试关闭 VPN 或代理

    **问题 4：关闭终端后服务停止**
    - Streamlit 需要在终端中持续运行
    - 关闭终端窗口会停止服务
    - 重新打开终端，重新执行启动命令即可

    **问题 5：修改代码后没有生效**
    - Streamlit 会自动检测文件变化并提示重新运行
    - 点击右上角的 "Rerun" 或刷新浏览器页面
    - 如果仍然不生效，停止服务（Ctrl+C）重新启动

    ---

    ### 安装依赖

    如果缺少依赖包，运行：

    ```
    pip install -r requirements.txt
    ```

    或使用虚拟环境：

    **Windows：**
    ```
    C:\\ProgramData\\WorkBuddy\\chromium-env\\6od91a\\.workbuddy\\binaries\\python\\envs\\default\\Scripts\\pip.exe install -r requirements.txt
    ```
    """)

