# Tab 1: 快速开始
# ============================================================
with tab1:
    st.subheader("5 分钟上手指南")

    st.markdown("""
    ### 第一步：配置

    1. 打开左侧 **⚙️ 配置** 页面
    2. 上传你的主简历（支持 PDF、DOCX、TXT）
    3. 点击「智能解析简历」，AI 会自动提取你的技能、经验、学历
    4. 在 **API 设置** 中选择 AI 提供商：
       - **DeepSeek**（推荐）：国产 AI，便宜好用，注册即用
       - **OpenAI**：效果最好，需要国外账号
       - **Ollama**：完全免费，运行在你自己的电脑上
    5. 填写 API Key 并点击「保存并测试」
    6. 在 **求职偏好** 中设置你想找的职位类型、行业、地点
    7. 在 **语言设置** 中选择是否生成中英双语版本

    ### 第二步：发现职位

    打开 **🔍 发现职位** 页面，有三种方式：

    **方式一：海外平台搜索**
    - 支持 LinkedIn、Indeed、Glassdoor 等平台
    - 输入搜索关键词（英文效果更好）
    - 选择目标国家和平台
    - 点击「开始搜索」

    **方式二：AI 公司发现**
    - 如果你不知道有哪些公司值得投
    - AI 会根据你的行业偏好推荐公司
    - 三层扩展：职位提取 → AI推荐 → 榜单搜索

    **方式三：手动粘贴 JD**
    - 从 Boss 直聘、猎聘等平台复制岗位描述
    - 粘贴到文本框，AI 自动解析并计算匹配度
    - 适合国内平台的职位

    ### 第三步：审核挑选

    打开 **📊 审核挑选** 页面：
    - 查看所有发现的岗位，按匹配度排序
    - 逐一审视，点击 ✅ 选择或 ❌ 跳过
    - 可以按公司名筛选、按匹配度过滤

    ### 第四步：生成简历

    打开 **✨ 生成简历** 页面：
    - 查看已选的岗位列表
    - 开启「中英双语」开关（如需要）
    - 点击「一键生成全部」，AI 会：
      1. 分析目标公司的风格类型
      2. 定制简历内容（突出匹配经验）
      3. 撰写 Cover Letter（适配公司语气）
      4. 生成中英文两个版本
    - 下载生成的 HTML 简历和 Markdown 求职信

    ### 第五步：投递追踪

    打开 **📈 投递追踪** 页面：
    - 添加投递记录
    - 更新投递状态（已投、面试、Offer 等）
    - 查看统计管道

    > 💡 **半自动的含义**：工具帮你发现、匹配、定制简历，但**最终投递由你手动完成**。
    > 这样既高效又安全，不会有封号风险。
    """)

